
#Creating a list
a=[1,2,3,4,5,6,7,8,9,0]

#printing a list
print(a)
# accessing the list using index
print(a[2])
#change the value of list using
a[0]=98
print(a)

# we can create a list with items of different types
c=[45,"Harry", False, 6.987]
print(c)

#list slicing
friends=["Harry","Tom","Rohan","Sam"]
